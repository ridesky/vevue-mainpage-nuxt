function formatTime(date) {
  var year = date.getFullYear()
  var month = date.getMonth() + 1
  var day = date.getDate()

  var hour = date.getHours()
  var minute = date.getMinutes()
  var second = date.getSeconds()


  return [year, month, day].map(formatNumber).join('-') + ' ' + [hour, minute, second].map(formatNumber).join(':')
}

function formatNumber(n) {
  n = n.toString()
  return n[1] ? n : '0' + n
};

function smartTime(timestamp, lang) {
  let minute = 60 * 1000; // 1 min
  let hour = 60 * minute; // 1 hour
  let day = 24 * hour; // 1 day
  if (!timestamp) {
    return null;
  };
  let long = new Date().getTime() - timestamp;
  if (long < minute) {
    return 'a minute ago';
  } else if (long <= hour) {
    return Math.ceil(long / minute) + ' mins ago';
  } else if (long < day) {
    return Math.ceil(long / hour) + ' hours ago';
  } else {
    return formatTime(timestamp);
  }
}

